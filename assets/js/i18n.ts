export function currentLocale(): Locale {
  if (typeof document === 'undefined') return 'en'

  const locale =
    document.documentElement.dataset.locale || document.documentElement.lang

  if (!locale) return 'en'
  return locale.toLowerCase().startsWith('fi') ? 'fi' : 'en'
}

export function t(
  key: TranslationKey,
  params: TranslationParams = {},
  locale: Locale = currentLocale()
): string {
  const catalog = locale === 'en' ? englishCatalog : finnishCatalog

  return Object.entries(params).reduce(
    (message, [name, value]) =>
      message.split(`{{${name}}}`).join(String(value)),
    catalog[key]
  )
}

export function tn(
  singular: TranslationKey,
  plural: TranslationKey,
  count: number,
  params: TranslationParams = {},
  locale: Locale = currentLocale()
): string {
  return t(count === 1 ? singular : plural, { ...params, count }, locale)
}

export function numberFormatter(locale: Locale = currentLocale()) {
  return new Intl.NumberFormat(locale === 'fi' ? 'fi-FI' : 'en-US')
}
